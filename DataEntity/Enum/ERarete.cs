namespace DataEntity
{
    // Énumération des niveaux de rareté pour les objets
    // Influence généralement la puissance et le prix des objets
    public enum ERarete
    {
        Commun,         // Objets de base, faciles à trouver
        PeuCommun,      // Objets décents, moyennement rares
        Rare,           // Bons objets, difficiles à obtenir
        Epique,         // Excellents objets, très rares
        Legendaire      // Objets exceptionnels, extrêmement rares
    }
}
